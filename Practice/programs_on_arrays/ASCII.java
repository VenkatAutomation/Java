package programs_on_arrays;

public class ASCII 
{
	
	public static void main(String[] args) {
		
		char ch1 = 'A';
		char ch2 = 'B';
		
		int ascii1=ch1;
		int ascii2=ch2;
		
		System.out.println("The ASCII Value of "+ch1+" is :"+ascii1);
		System.out.println("The ASCII Value of "+ch2+" is :"+ascii2);
		
	}

}
